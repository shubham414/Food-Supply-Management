    public void populateFoodRequest() {
        DefaultTableModel dtm = (DefaultTableModel) tblNotifyQualityChecker.getModel();
        dtm.setRowCount(0);

        Object row[] = new Object[7];
        row[0] = foodReq.getFood().getName();
        row[1] = foodReq.getFood().getCode();
        dtm.addRow(row);
    }

    public void populateTechnicianRequest() {
        DefaultTableModel dtm = (DefaultTableModel) tblNotifyQualityChecker.getModel();
        dtm.setRowCount(0);

        Object row[] = new Object[7];
        row[0] = qualCheckReq.getFood().getName();
        row[1] = qualCheckReq.getFood().getCode();
        row[2] = qualCheckReq.getStatus();
        row[3] = qualCheckReq.getReceiver();
        row[4] = ((QualityCheckRequest) qualCheckReq).getServiceDateInString();

        dtm.addRow(row);
    }

