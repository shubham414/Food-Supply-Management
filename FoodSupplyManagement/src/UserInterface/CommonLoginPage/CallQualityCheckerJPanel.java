    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblNotifyQualityChecker.getSelectedRow();
        if (selectedRow >= 0) {
            String des = txtProblemDesc.getText();
            String type = "";
            if (des.equals("")) {
                JOptionPane.showMessageDialog(null, "Please enter Description");
                return;
            } else {
                qualCheckReq = new QualityCheckRequest();
                qualCheckReq.setFood(foodReq.getFood());
                qualCheckReq.setSender(ua);
                org.getWorkQueue().getWorkRequestList().add(qualCheckReq);
                ua.getWorkQueue().getWorkRequestList().add(qualCheckReq);
                system.getWorkQueue().getWorkRequestList().add(qualCheckReq);
                qualCheckReq.setDescription(des);

                qualCheckReq.setStatus("Requested");
                qualCheckReq.setSender(ua);
                populateTechnicianRequest();
                JOptionPane.showMessageDialog(null, "Request send successfully");

            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select the request!!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnSendActionPerformed

