    public void populateSupplierTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblSupplierFoodDetails.getModel();
        dtm.setRowCount(0);
        for (Food m : s.getFoodList().getFoodCatalog()) {
            Object row[] = new Object[4];
            row[0] = m;
            row[1] = m.getCode();
            row[2] = m.getQuantity();

            dtm.addRow(row);
        }
    }
    private void btnAddFoodActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddFoodActionPerformed

        String foodName = txtFoodName.getText();
        String xz = txtQuantity.getText();
        try {
            if ((foodName.equals("")) || (xz.equals(""))) {
                throw new IllegalArgumentException();
            }
            try {
                int qty = Integer.parseInt(txtQuantity.getText());

                Food.FoodType type = (Food.FoodType) jcbFoodType.getSelectedItem();

                int temp = 0;

                if (type == null) {
                    JOptionPane.showMessageDialog(null, "Please select a Food type");
                    return;
                }

                for (Food m : s.getFoodList().getFoodCatalog()) {
                    if (foodName.equals(m.getName())) {
                        temp = 1;
                        m.setQuantity(m.getQuantity() + qty);
                    }
                }
                if (temp == 0) {
                    Food m = s.getFoodList().addFood();
                    m.setName(foodName);
                    m.setQuantity(qty);

                    m.setType(type.getValue());
                    Date today = new Date();
                    DateFormat oDateFormat = new SimpleDateFormat("MM-dd-yyyy");
                    String todayDate = oDateFormat.format(today);
                    m.setDateOfEntry(today);
                    m.setDateOfEntryInString(todayDate);
                }
                populateSupplierTable();
                txtFoodName.setText("");

                txtQuantity.setText("");

            } catch (NumberFormatException n) {

            }
        } catch (IllegalArgumentException I) {
            JOptionPane.showMessageDialog(null, "Please input value");
        }
    }//GEN-LAST:event_btnAddFoodActionPerformed

