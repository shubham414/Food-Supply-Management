    private void btnDeleteFoodActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteFoodActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblSupplierFoodDetails.getSelectedRow();
        food = (Food) tblSupplierFoodDetails.getValueAt(selectedRow, 0);
        System.out.println(selectedRow);
        if (selectedRow >= 0) {
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to delete this food?", "Warning", dialogButton);
            if (dialogResult == JOptionPane.YES_OPTION) {

                s.getFoodList().removeFood(food);
                populateSupplierTable();

            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row", "Warning", JOptionPane.WARNING_MESSAGE);

        }
    }//GEN-LAST:event_btnDeleteFoodActionPerformed


